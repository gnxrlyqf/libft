#include "libft.h"

char	*ft_strdup(char *src)
{
	int		i;
	char	*new;

	if (!src)
		return (NULL);
	new = malloc((ft_strlen(src) + 1) * sizeof(char));
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
